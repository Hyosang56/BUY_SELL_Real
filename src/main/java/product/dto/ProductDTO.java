package product.dto;

public class ProductDTO {
	private String p_id;
	private String p_name;
	private int p_price;
	private String p_description;
	private String p_fileName;
	private String userid;
	private String buy_userid;
	private int buycheck;
	
	
	
	public String getP_id() {
		return p_id;
	}
	public void setP_id(String p_id) {
		this.p_id = p_id;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public int getP_price() {
		return p_price;
	}
	public void setP_price(int p_price) {
		this.p_price = p_price;
	}
	public String getP_description() {
		return p_description;
	}
	public void setP_description(String p_description) {
		this.p_description = p_description;
	}
	public String getP_fileName() {
		return p_fileName;
	}
	public void setP_fileName(String p_fileName) {
		this.p_fileName = p_fileName;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getBuy_userid() {
		return buy_userid;
	}
	public void setBuy_userid(String buy_userid) {
		this.buy_userid = buy_userid;
	}
	public int getBuycheck() {
		return buycheck;
	}
	public void setBuycheck(int buycheck) {
		this.buycheck = buycheck;
	}
	
	
	
	

}
